pub const fn lut_for_mode(mode: RefreshMode) -> &'static LutBank {
    match mode {
        RefreshMode::Full => &LUT_FULL,
        RefreshMode::Fast => &LUT_FAST,
        RefreshMode::FastClean | RefreshMode::PowerDown => &LUT_HALF,
    }
}
