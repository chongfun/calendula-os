pub const CMD_SOURCE_LV_SELECT: u8 = 0xE1;

pub const DEEP_SLEEP_CHECK: u8 = 0xA5;

pub const INIT_PSR: [u8; 2] = [0x3F, 0x0A];
// The OEM programs 792x600 even though the visible panel is 792x528.
pub const INIT_TRES: [u8; 4] = [0x03, 0x18, 0x02, 0x58];
pub const INIT_GSST: [u8; 4] = [0x00, 0x00, 0x00, 0x00];
pub const INIT_PFS: [u8; 1] = [0x20];
pub const INIT_PWR: [u8; 5] = [0x07, 0x17, 0x3F, 0x3F, 0x17];
pub const INIT_VDCS: [u8; 1] = [0x24];
pub const INIT_BTST: [u8; 4] = [0x25, 0x25, 0x3C, 0x37];
pub const INIT_PLL: [u8; 1] = [0x09];
pub const INIT_LV_SEL: [u8; 1] = [0x02];

pub static INIT_SEQUENCE: &[SpiOp] = &[
    SpiOp::Reset,
    SpiOp::Command {
        cmd: CMD_PANEL_SETTING,
        data: &INIT_PSR,
    },
    SpiOp::Command {
        cmd: CMD_RESOLUTION,
        data: &INIT_TRES,
    },
    SpiOp::Command {
        cmd: CMD_GATE_SOURCE_START,
        data: &INIT_GSST,
    },
    SpiOp::Command {
        cmd: CMD_POWER_OFF_SEQUENCE,
        data: &INIT_PFS,
    },
    SpiOp::Command {
        cmd: CMD_POWER_SETTING,
        data: &INIT_PWR,
    },
    SpiOp::Command {
        cmd: CMD_VCOM_DC,
        data: &INIT_VDCS,
    },
    SpiOp::Command {
        cmd: CMD_BOOSTER_SOFT_START,
        data: &INIT_BTST,
    },
    SpiOp::Command {
        cmd: CMD_PLL,
        data: &INIT_PLL,
    },
    SpiOp::Command {
        cmd: CMD_SOURCE_LV_SELECT,
        data: &INIT_LV_SEL,
    },
];

