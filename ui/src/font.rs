/// Draws a single character using the embedded 8x8 bitmap font.
pub fn draw_char(fb: &mut Framebuffer, c: char, x: usize, y: usize, white: bool) {
    let ascii = c as usize;
    if !(32..=126).contains(&ascii) {
        return; // Only ASCII characters 32-126 are supported
    }
    let font_idx = (ascii - 32) * GLYPH_HEIGHT;
    for row in 0..GLYPH_HEIGHT {
        let row_data = FONT_DATA[font_idx + row];
        for col in 0..GLYPH_WIDTH {
            if (row_data & (1 << (7 - col))) != 0 {
                fb.set_pixel(x + col, y + row, white);
            }
        }
    }
}

/// Draws an entire string by wrapping character positions.
pub fn draw_str(fb: &mut Framebuffer, s: &str, mut x: usize, y: usize, white: bool) {
    for c in s.chars() {
        draw_char(fb, c, x, y, white);
        x += GLYPH_WIDTH;
    }
}
